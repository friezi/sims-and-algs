package de.zintel.gfx.g2d;

/**
 * @author Friedemann
 *
 */
public class Field {

	public double minX;

	public double minY;

	public double maxX;

	public double maxY;

	public Field(double minX, double minY, double maxX, double maxY) {
		super();
		this.minX = minX;
		this.minY = minY;
		this.maxX = maxX;
		this.maxY = maxY;
	}

}
